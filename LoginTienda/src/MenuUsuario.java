    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed

        String nombre = jComboBox1.getSelectedItem().toString();
    double precio = 0;

    switch (nombre) {
        case "Laptop": precio = 2000; break;
        case "Mouse": precio = 50; break;
        case "Teclado": precio = 100; break;
        case "Audífonos": precio = 150; break;
    }

    Producto p = new Producto(nombre, precio);
    deseos.agregar(p);

    javax.swing.JOptionPane.showMessageDialog(this, "Agregado a deseos: " + nombre);
    }//GEN-LAST:event_jButton6ActionPerformed

